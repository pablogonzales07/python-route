# Operadores aritméticos

x = 5
y = 12

# Suma + 
print("Suma", x + y)

# Resta -
print("Resta", x - y)

# Multiplicación *
print("Multiplicación", x * y)

# División / (retorna un float)
print("División", y / x)

# Módulo %
print("Módulo(resto)", y % x)

# Potencia **
print("Potencia", y ** x) # 10 * 10 * 10 *10 *10

# División entera //
print("División Entera", y // x)

par = 7
print(par % 2 == 0)

